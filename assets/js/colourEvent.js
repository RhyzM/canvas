
// Colour Change Events Function

$(`.blackI`).click(function (){
    style.selectedStrokeColour = '#000000'
    style.selectedFillColour = '#000000'
})

$(`.whiteI`).click(function (){
    style.selectedStrokeColour= '#ffffff'
    style.selectedFillColour= '#ffffff'
})

